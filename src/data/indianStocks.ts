// Helper function to search stocks
export const searchStocks = (query: string, limit: number = 10): StockItem[] => {
  if (!query || query.length < 1) return [];
  
  const searchTerm = query.toLowerCase().trim();
  
  const results = indianStocks.filter(stock => 
    stock.symbol.toLowerCase().includes(searchTerm) ||
    stock.name.toLowerCase().includes(searchTerm)
  );
  
  // Sort by relevance: exact symbol matches first, then name matches
  results.sort((a, b) => {
    const aSymbolMatch = a.symbol.toLowerCase().startsWith(searchTerm);
    const bSymbolMatch = b.symbol.toLowerCase().startsWith(searchTerm);
    
    if (aSymbolMatch && !bSymbolMatch) return -1;
    if (!aSymbolMatch && bSymbolMatch) return 1;
    
    return a.name.localeCompare(b.name);
  });
  
  return results.slice(0, limit);
};
